# While Loop

# Increment Values
number = 5
while number <= 10 :
    print(number)
    number += 1

#  Decrement Values
number = 30
while number >= 25 :
    print(number)
    number -= 1

# For Loop
for x in range(105,110,2) :
    print("number is ", x)

for x in range(30,24,-1) :
    print("number is ", x)

devices = ["laptop","mouse","computer"]
for y in devices :
    print(y)